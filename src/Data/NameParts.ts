export const nameParts = ([] as NamePart[]).concat(
  namePartsFood,
  namePartsTitle,
  namePartsProfession
);

export const buildNameDataTree = () => {
  return nameParts.reduce(
    (object, catNamePart) => {
      const startingChar = catNamePart.content.charAt(0).toLowerCase() as Char;

      if (!object.data[catNamePart.category]) {
        object.data[catNamePart.category] = {};
      }
      if (!object.data[catNamePart.category]![startingChar]) {
        object.data[catNamePart.category]![startingChar] = [];
      }
      object.data[catNamePart.category]![startingChar]!.push(catNamePart);
      return object;
    },
    {
      data: {},
    } as NamePartTree
  );
};
